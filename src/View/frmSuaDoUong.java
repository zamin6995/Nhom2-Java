    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        String id = this.txtID.getText();
        String ten = this.txtTenDoUong.getText();
        String dongia = this.txtDonGia.getText();

        if(ten.equals("")){
            JOptionPane.showMessageDialog(this, "Chưa nhập tên đồ uống");
            return;
        }
        if(dongia.equals("")){
            JOptionPane.showMessageDialog(this, "Chưa nhập đơn giá");
            return;
        }
        if(ktso(dongia) == false){
            JOptionPane.showMessageDialog(this, "Đơn giá phải là số");
            return;
        }
        int ketqua = CSDL.tbDoUong.SuaDoUong(Integer.parseInt(id), ten, Float.parseFloat(dongia));
        if(ketqua == -1){
            JOptionPane.showMessageDialog(this, "Loi ket noi sql");
            return;
        }
        else if(ketqua == -2){
            JOptionPane.showMessageDialog(this, "Loi sql");
            return;
        }
        else{
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            this.dispose();
            csdouong.HienThiDSDoUong();
        }

    }//GEN-LAST:event_btnSuaActionPerformed

