    public void HienThiDSHoaDon(){
        Connection conn = CSDL.Database.KetNoiCSDL();
        if(conn == null)
        {
            JOptionPane.showMessageDialog(this, "Loi ket noi");
            return;
        }
        String soban = this.txtSoBan.getText();
        String sql = "select * from hoadon_ban_" + soban;
        
        
        try {
            PreparedStatement stm = conn.prepareStatement(sql);
            ResultSet rs = stm.executeQuery();
            DefaultTableModel dtm = (DefaultTableModel)this.tblHoaDon.getModel();
            dtm.setNumRows(0);
            while(rs.next()){
                int id = rs.getInt("id");
                String ten = rs.getString("ten");
                float dongia = rs.getFloat("dongia");
                int soluong = rs.getInt("soluong");
                float thanhtien = rs.getFloat("thanhtien"); 
                dtm.addRow(new Object[] {id, ten, Float.toString(dongia), Integer.toString(soluong), Float.toString(thanhtien)});
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(frmChonMon.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Loi sql " + ex.getMessage());
            return;
        }
    }
    
