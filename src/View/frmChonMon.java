    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int i = this.tblHoaDon.getSelectedRow();
        if(i<0){
            JOptionPane.showMessageDialog(this, "Chưa chọn món cần xóa");
            return;
        }
        else{
            Connection cnn = CSDL.Database.KetNoiCSDL();
            if(cnn == null){
                JOptionPane.showMessageDialog(this, "Ket noi that bai !!!");
                return;
            }
            String soban = this.txtSoBan.getText();
            String sql = "DELETE FROM hoadon_ban_" + soban 
                    + " WHERE ID=?";
            try {
                DefaultTableModel dtm = (DefaultTableModel)this.tblHoaDon.getModel();
                int id = (int)dtm.getValueAt(i, 0);
                PreparedStatement stm = cnn.prepareStatement(sql);
                stm.setInt(1, id);
                int n = stm.executeUpdate();
                if(n>0){
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    HienThiDSHoaDon();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Loi SQL");
                Logger.getLogger(frmChonMon.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
            
    }//GEN-LAST:event_btnXoaActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Connection conn = CSDL.Database.KetNoiCSDL();
        if(conn == null)
        {
            JOptionPane.showMessageDialog(this, "Loi ket noi");
            return;
        }
        int hoi = JOptionPane.showConfirmDialog(this, "Bạn sẽ bị mất hết dữ liệu.\nChắc chắn xóa ??", "Thông báo !!!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if(hoi == JOptionPane.YES_OPTION){
            String soban = this.txtSoBan.getText();
            String sql = "delete from hoadon_ban_" + soban;

            try {
                PreparedStatement stm = conn.prepareStatement(sql);
                int n = stm.executeUpdate();
                if(n>0){
                    JOptionPane.showMessageDialog(this, "Xóa hết thành công");
                }
            } catch (SQLException ex) {
                Logger.getLogger(frmThanhToan.class.getName()).log(Level.SEVERE, null, ex);
            }

            String sqll = "ALTER TABLE hoadon_ban_" + soban + " AUTO_INCREMENT = 1";

            try {
                PreparedStatement stm = conn.prepareStatement(sqll);
                stm.executeUpdate();
            } catch (SQLException ex) {
                Logger.getLogger(frmThanhToan.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Lôi sql " + ex.getMessage());
            }
            HienThiDSHoaDon();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

