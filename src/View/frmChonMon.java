    private void btnThemMonHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMonHoaDonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)this.tblHoaDon.getModel();
        String ten = this.txtTenMonAn.getText();
        String dongia = this.txtDonGiaMonAn.getText();
        String soluong = this.txtSoLuongMonAn.getText();
        String thanhtien = this.txtThanhTienMonAn.getText();
        
        if(thanhtien.equals("")){
            JOptionPane.showMessageDialog(this, "Chưa thành tiền");
            return;
        }
        
        Connection conn = CSDL.Database.KetNoiCSDL();
        if(conn == null)
        {
            JOptionPane.showMessageDialog(this, "Loi ket noi");
            return;
        }
        String soban = this.txtSoBan.getText();
        String sql = "insert into hoadon_ban_" + soban + "(ID, Ten, DonGia, SoLuong, ThanhTien) "
                + " values(NULL, ?, ?, ?, ?)";

            PreparedStatement stm;
        try {
            stm = conn.prepareStatement(sql);
            stm.setString(1, ten);
            stm.setFloat(2, Float.parseFloat(dongia));
            stm.setInt(3, Integer.parseInt(soluong));
            stm.setFloat(4, Float.parseFloat(thanhtien));
 
            int n = stm.executeUpdate();
            if(n>0){
                JOptionPane.showMessageDialog(this, "Thêm thành công");
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmChonMon.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Loi sql " + ex.getMessage());
            return;
        }
        
        HienThiDSHoaDon();
          
        this.txtTenMonAn.setText("");
        this.txtDonGiaMonAn.setText("");
        this.txtSoLuongMonAn.setText("");
        this.txtThanhTienMonAn.setText("");
        
        HienThiDSMonAn();
        
    }//GEN-LAST:event_btnThemMonHoaDonActionPerformed

